     * X component of this vector.
     * @since 1.1.0
     */
    public var x: Number

    /**
     * Y component of this vector.
     * @since 1.1.0
     */
    public var y: Number

    /**
     * Z component of this vector.
     * @since 1.1.0
     */
    public var z: Number
}
